def quick_sort(arr, depth=0):
    indent = "  " * depth
    if len(arr) <= 1:
        return arr

    pivot = arr[0]
    left = []
    right = []

    for i in range(1, len(arr)):
        if arr[i] < pivot:
            left.append(arr[i])
        else:
            right.append(arr[i])


    print(f"{indent}ç›®å‰é™£åˆ—ï¼š{arr}")
